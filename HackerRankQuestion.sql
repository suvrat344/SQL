-- 20. Write a query identifying the type of each record in the TRIANGLES table using its three side lengths. Output one of the following
-- statements for each record in the table:
-- 1. Equilateral: It's a triangle with  sides of equal length.
-- 2. Isosceles: It's a triangle with  sides of equal length.
-- 3. Scalene: It's a triangle with  sides of differing lengths.
-- 4. Not A Triangle: The given values of A, B, and C don't form a triangle.
SELECT
CASE 
	WHEN a + b <= c OR a + c <= b OR b + c <= a THEN "Not A Triangle"
	WHEN a = b AND a = c  THEN "Equilateral"
	WHEN a = b OR b = c OR c = a THEN "Isosceles"
    ELSE "Scalene"
END AS TriangleType
FROM 
	triangle;


-- 21. You are given a table, BST, containing two columns: N and P, where N represents the value of a node in Binary Tree, and P is the 
-- parent of N.
-- Write a query to find the node type of Binary Tree ordered by the value of the node. Output one of the following for each node:
-- 1. Root: If node is root node.
-- 2. Leaf: If node is leaf node.
-- 3. Inner: If node is neither root nor leaf node
SELECT 
	DISTINCT n,
	CASE 
		WHEN n IN (SELECT n FROM bst WHERE p IS NULL) THEN "Root"
		WHEN n NOT IN  (SELECT DISTINCT p FROM bst WHERE p IS NOT NULL) THEN "Leaf" 
        WHEN n IN (SELECT DISTINCT p FROM bst WHERE p IS NOT NULL) THEN "Inner"
	END AS result
 FROM 
	bst 
ORDER BY
	n;
    
    
-- 22. Amber's conglomerate corporation just acquired some new companies. Each of the companies follows this hierarchy: 
-- founder -> Lead Manager -> Senior Manager -> Manager -> Employee
-- Given the table schemas below, write a query to print the company_code, founder name, total number of lead managers, total number of 
-- senior managers, total number of managers, and total number of employees. Order your output by ascending company_code.
-- Note:
-- 1. The tables may contain duplicate records.
-- 2. The company_code is string, so the sorting should not be numeric. For example, if the company_codes are C_1, C_2, and C_10, then 
-- the ascending company_codes will be C_1, C_10, and C_2.
SELECT 
    c.company_code,
    c.founder,
    COUNT(DISTINCT lm.lead_manager_code) AS total_lead_managers,
    COUNT(DISTINCT sm.senior_manager_code) AS total_senior_managers,
    COUNT(DISTINCT m.manager_code) AS total_managers,
    COUNT(DISTINCT e.employee_code) AS total_employees
FROM 
    company c
INNER JOIN 
    lead_manager lm ON c.company_code = lm.company_code
INNER JOIN 
    senior_manager sm ON c.company_code = sm.company_code
INNER JOIN 
    manager m ON c.company_code = m.company_code
INNER JOIN 
    employee e ON c.company_code = e.company_code
GROUP BY 
    c.company_code, c.founder
ORDER BY 
    c.company_code ASC;
    
    
